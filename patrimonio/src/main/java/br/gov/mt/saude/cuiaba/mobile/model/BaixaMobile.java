package br.gov.mt.saude.cuiaba.mobile.model;


public class BaixaMobile {
	private long idbaixa;
	private String databaixa;
	private String horabaixa;
	private int idusario;
	private String idpatrimonio;
	private int flag;
	public long getIdbaixa() {
		return idbaixa;
	}
	public void setIdbaixa(long idbaixa) {
		this.idbaixa = idbaixa;
	}
	public String getDatabaixa() {
		return databaixa;
	}
	public void setDatabaixa(String databaixa) {
		this.databaixa = databaixa;
	}
	public String getHorabaixa() {
		return horabaixa;
	}
	public void setHorabaixa(String horabaixa) {
		this.horabaixa = horabaixa;
	}
	public int getIdusario() {
		return idusario;
	}
	public void setIdusario(int idusario) {
		this.idusario = idusario;
	}
	public String getIdpatrimonio() {
		return idpatrimonio;
	}
	public void setIdpatrimonio(String idpatrimonio) {
		this.idpatrimonio = idpatrimonio;
	}
	public int getFlag() {
		return flag;
	}
	public void setFlag(int flag) {
		this.flag = flag;
	}
	
	
}
