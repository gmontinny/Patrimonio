package br.gov.mt.saude.cuiaba.mobile.model;

public class PatrimonioMobile {
	private String idpatrimonio;
	private int idmaterial;
	private int idmodelo;
	private int idlocal;;
	private int idfornecedor;
	private int numeroarquivo;
	private int situacaopatrimonio;
	private String serialpatrimonio;
	private int statusbaixa;
	private String obspatrimonio;
	public String getIdpatrimonio() {
		return idpatrimonio;
	}
	public void setIdpatrimonio(String idpatrimonio) {
		this.idpatrimonio = idpatrimonio;
	}
	public int getIdmaterial() {
		return idmaterial;
	}
	public void setIdmaterial(int idmaterial) {
		this.idmaterial = idmaterial;
	}
	public int getIdmodelo() {
		return idmodelo;
	}
	public void setIdmodelo(int idmodelo) {
		this.idmodelo = idmodelo;
	}
	public int getIdlocal() {
		return idlocal;
	}
	public void setIdlocal(int idlocal) {
		this.idlocal = idlocal;
	}
	public int getIdfornecedor() {
		return idfornecedor;
	}
	public void setIdfornecedor(int idfornecedor) {
		this.idfornecedor = idfornecedor;
	}
	public int getNumeroarquivo() {
		return numeroarquivo;
	}
	public void setNumeroarquivo(int numeroarquivo) {
		this.numeroarquivo = numeroarquivo;
	}
	public int getSituacaopatrimonio() {
		return situacaopatrimonio;
	}
	public void setSituacaopatrimonio(int situacaopatrimonio) {
		this.situacaopatrimonio = situacaopatrimonio;
	}
	public String getSerialpatrimonio() {
		return serialpatrimonio;
	}
	public void setSerialpatrimonio(String serialpatrimonio) {
		this.serialpatrimonio = serialpatrimonio;
	}
	public int getStatusbaixa() {
		return statusbaixa;
	}
	public void setStatusbaixa(int statusbaixa) {
		this.statusbaixa = statusbaixa;
	}
	public String getObspatrimonio() {
		return obspatrimonio;
	}
	public void setObspatrimonio(String obspatrimonio) {
		this.obspatrimonio = obspatrimonio;
	}
	
	
}
